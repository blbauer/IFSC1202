# Read an integer
a = int(input("Number: "))
# Calculate Previous and Next Number
previousnumber = a - 1
nextnumber = a + 1
# Print the results
print("The next number for the number " + str(a) + " is " + str(nextnumber) + ".")
print("The previous number for the number " + str(a) + " is " + str(previousnumber) + ".")
